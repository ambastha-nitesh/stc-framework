"""
STC Framework - Operational Control & Cost Management

Enterprise-grade FinOps controls for AI agent systems:

1. TOKEN GOVERNANCE
   - Max input/output tokens per request
   - Context window limits
   - Token budget per persona per day/month

2. RATE LIMITING
   - Requests per minute/hour per persona
   - Concurrent request caps
   - Backpressure with queuing

3. BURST CONTROL
   - Runaway loop detection (agent stuck in retry cycles)
   - Max tool calls per request
   - Max LLM calls per workflow execution
   - Automatic circuit breaker on loop detection

4. COST CIRCUIT BREAKERS
   - Real-time spend tracking against budgets
   - Pre-request cost estimation (before the LLM call, not after)
   - Graduated response: warn → throttle → pause → halt
   - Per-persona budget allocation (Stalwart 80%, Trainer 15%, Critic 5%)

5. COST ANOMALY DETECTION
   - Rolling average cost per request type
   - Alert when a request exceeds N× the rolling average
   - Pattern detection for cost drift over time

6. QUOTA MANAGEMENT
   - Daily/monthly quotas per persona
   - Quota reservation for critical operations
   - Graceful degradation when approaching limits

All controls produce audit events and feed into the Trainer's
optimization loop (the Trainer learns to reduce costs over time).

Usage:
    from operational.cost_controls import OperationalController
    controller = OperationalController(spec)

    # Before making an LLM call:
    approval = controller.pre_request_check(
        persona="stalwart", model="claude-sonnet",
        estimated_input_tokens=2000, estimated_output_tokens=500
    )
    if not approval.approved:
        # Handle: throttle, queue, or reject

    # After the LLM call:
    controller.record_usage(
        persona="stalwart", model="claude-sonnet",
        input_tokens=1850, output_tokens=420, cost_usd=0.008,
        latency_ms=1200
    )
"""

