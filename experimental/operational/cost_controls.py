class BurstController:
    """
    Detects and prevents runaway agent loops.
    
    A "runaway" agent might get stuck retrying the same failed operation,
    burning through budget in minutes. The burst controller detects:
    - Too many LLM calls in a single workflow execution
    - Too many tool calls in a single request
    - Rapid repeated identical requests (stuck loop)
    """

    def __init__(self, spec: STCSpec):
        self.spec = spec
        burst_config = spec.trainer.get("burst_control", {})

        self.max_llm_calls_per_workflow = burst_config.get("max_llm_calls", 10)
        self.max_tool_calls_per_request = burst_config.get("max_tool_calls", 20)
        self.duplicate_window_seconds = burst_config.get("duplicate_window", 60)

        # Tracking
        self.workflow_call_counts: dict[str, int] = defaultdict(int)
        self.recent_requests: deque = deque(maxlen=100)

    def check_workflow(self, workflow_id: str) -> tuple[bool, str]:
        """Check if a workflow has exceeded its LLM call limit."""
        count = self.workflow_call_counts.get(workflow_id, 0)
        if count >= self.max_llm_calls_per_workflow:
            return False, f"Workflow {workflow_id} exceeded max LLM calls ({self.max_llm_calls_per_workflow})"
        return True, "Within limits"

    def record_llm_call(self, workflow_id: str):
        self.workflow_call_counts[workflow_id] = self.workflow_call_counts.get(workflow_id, 0) + 1

    def reset_workflow(self, workflow_id: str):
        self.workflow_call_counts.pop(workflow_id, None)

    def check_duplicate(self, query_hash: str) -> tuple[bool, str]:
        """Detect rapid duplicate requests (stuck loop indicator)."""
        now = time.time()
        cutoff = now - self.duplicate_window_seconds

        recent_same = sum(
            1 for ts, qh in self.recent_requests
            if qh == query_hash and ts > cutoff
        )

        if recent_same >= 3:
            return False, f"Duplicate request detected {recent_same} times in {self.duplicate_window_seconds}s (possible stuck loop)"

        self.recent_requests.append((now, query_hash))
        return True, "No duplicate detected"


