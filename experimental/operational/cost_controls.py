@dataclass
class PreRequestApproval:
    """Result of a pre-request cost check."""
    approved: bool
    action: str          # allow | warn | throttle | queue | reject
    reason: str
    estimated_cost_usd: float
    budget_remaining_usd: float
    wait_seconds: float = 0.0  # If throttled, how long to wait
    constraints: dict = field(default_factory=dict)


@dataclass
class UsageRecord:
    """Record of a single LLM/tool usage event."""
    timestamp: str
    persona: str
    model: str
    input_tokens: int
    output_tokens: int
    total_tokens: int
    cost_usd: float
    latency_ms: float
    request_type: str = "completion"  # completion | embedding | tool_call


@dataclass
class CostAlert:
    """Alert generated by cost controls."""
    timestamp: str
    alert_type: str     # budget_warning | budget_exceeded | anomaly | burst | rate_limit
    severity: str       # info | warning | critical
    persona: str
    details: str
    current_spend: float
    budget_limit: float
    evidence: dict = field(default_factory=dict)


