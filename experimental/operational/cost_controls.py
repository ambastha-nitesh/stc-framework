class TokenGovernor:
    """
    Enforces token limits per request and per persona.
    Prevents oversized requests from consuming disproportionate budget.
    """

    def __init__(self, spec: STCSpec):
        self.spec = spec
        token_config = spec.trainer.get("token_governance", {})

        self.max_input_tokens = token_config.get("max_input_tokens", 8000)
        self.max_output_tokens = token_config.get("max_output_tokens", 4000)
        self.max_context_window = token_config.get("max_context_window", 16000)

    def check(self, input_tokens: int, output_tokens: int) -> tuple[bool, str]:
        """Check if token counts are within governance limits."""
        if input_tokens > self.max_input_tokens:
            return False, f"Input tokens ({input_tokens}) exceed limit ({self.max_input_tokens})"
        if output_tokens > self.max_output_tokens:
            return False, f"Output tokens ({output_tokens}) exceed limit ({self.max_output_tokens})"
        if input_tokens + output_tokens > self.max_context_window:
            return False, f"Total tokens ({input_tokens + output_tokens}) exceed context window ({self.max_context_window})"
        return True, "Within limits"


