class CostCircuitBreaker:
    """
    Real-time cost governance with graduated response.
    
    Tracks spend against budgets and takes action BEFORE the budget
    is exhausted, not after.
    
    Response levels:
      0-80%  budget: allow (normal operation)
      80-90% budget: warn (log alerts, notify operator)
      90-95% budget: throttle (add delays between requests)
      95-99% budget: pause (queue requests, require approval)
      100%+  budget: halt (reject all requests)
    """

    def __init__(self, spec: STCSpec):
        self.spec = spec
        cost_config = spec.trainer.get("cost_thresholds", {})

        self.daily_budget = cost_config.get("daily_budget_usd", 50.0)
        self.monthly_budget = cost_config.get("monthly_budget_usd", 1200.0)
        self.max_per_task = cost_config.get("max_per_task_usd", 0.05)
        self.alert_percent = cost_config.get("alert_at_percent", 80)

        # Per-persona allocation (from spec or defaults)
        allocations = spec.trainer.get("budget_allocation", {})
        self.persona_allocation = {
            "stalwart": allocations.get("stalwart", 0.80),
            "trainer": allocations.get("trainer", 0.15),
            "critic": allocations.get("critic", 0.05),
        }

        # Spend tracking
        self.daily_spend: dict[str, float] = defaultdict(float)
        self.monthly_spend: dict[str, float] = defaultdict(float)
        self.last_reset_day: Optional[str] = None
        self.last_reset_month: Optional[str] = None
        self.alerts: list[CostAlert] = []

        self._check_reset()

    def _check_reset(self):
        """Reset daily/monthly counters as needed."""
        today = datetime.now(timezone.utc).strftime("%Y-%m-%d")
        month = datetime.now(timezone.utc).strftime("%Y-%m")

        if self.last_reset_day != today:
            self.daily_spend = defaultdict(float)
            self.last_reset_day = today

        if self.last_reset_month != month:
            self.monthly_spend = defaultdict(float)
            self.last_reset_month = month

    def check(self, persona: str, estimated_cost: float) -> tuple[str, str, float]:
        """
        Check if a request should proceed given current spend levels.
        Returns (action, reason, wait_seconds).
        
        Actions: allow | warn | throttle | pause | halt
        """
        self._check_reset()

        # Calculate persona-specific budget
        persona_daily_budget = self.daily_budget * self.persona_allocation.get(persona, 0.80)
        current_daily = self.daily_spend.get(persona, 0.0)
        projected = current_daily + estimated_cost

        # Check per-task limit
        if estimated_cost > self.max_per_task:
            return "warn", f"Estimated cost ${estimated_cost:.4f} exceeds per-task limit ${self.max_per_task:.4f}", 0.0

        # Calculate utilization
        utilization = projected / max(persona_daily_budget, 0.01)

        if utilization >= 1.0:
            self._alert("budget_exceeded", "critical", persona,
                       f"Daily budget exhausted: ${current_daily:.2f} / ${persona_daily_budget:.2f}",
                       current_daily, persona_daily_budget)
            return "halt", f"Budget exhausted ({utilization:.0%})", 0.0

        elif utilization >= 0.95:
            self._alert("budget_warning", "critical", persona,
                       f"Budget at {utilization:.0%}: ${current_daily:.2f} / ${persona_daily_budget:.2f}",
                       current_daily, persona_daily_budget)
            return "pause", f"Budget at {utilization:.0%} — requests paused", 30.0

        elif utilization >= 0.90:
            self._alert("budget_warning", "warning", persona,
                       f"Budget at {utilization:.0%}", current_daily, persona_daily_budget)
            return "throttle", f"Budget at {utilization:.0%} — throttling", 5.0

        elif utilization >= (self.alert_percent / 100):
            self._alert("budget_warning", "info", persona,
                       f"Budget at {utilization:.0%}", current_daily, persona_daily_budget)
            return "warn", f"Budget at {utilization:.0%}", 0.0

        return "allow", f"Budget OK ({utilization:.0%})", 0.0

    def record_spend(self, persona: str, cost_usd: float):
        """Record actual spend after a request completes."""
        self._check_reset()
        self.daily_spend[persona] = self.daily_spend.get(persona, 0.0) + cost_usd
        self.monthly_spend[persona] = self.monthly_spend.get(persona, 0.0) + cost_usd

    def get_budget_status(self) -> dict:
        """Get current budget utilization across all personas."""
        self._check_reset()
        status = {}
        for persona, allocation in self.persona_allocation.items():
            daily_budget = self.daily_budget * allocation
            daily_spent = self.daily_spend.get(persona, 0.0)
            monthly_budget = self.monthly_budget * allocation
            monthly_spent = self.monthly_spend.get(persona, 0.0)

            status[persona] = {
                "daily_budget": daily_budget,
                "daily_spent": daily_spent,
                "daily_utilization": daily_spent / max(daily_budget, 0.01),
                "monthly_budget": monthly_budget,
                "monthly_spent": monthly_spent,
                "monthly_utilization": monthly_spent / max(monthly_budget, 0.01),
            }

        status["total"] = {
            "daily_budget": self.daily_budget,
            "daily_spent": sum(self.daily_spend.values()),
            "daily_utilization": sum(self.daily_spend.values()) / max(self.daily_budget, 0.01),
            "monthly_budget": self.monthly_budget,
            "monthly_spent": sum(self.monthly_spend.values()),
            "monthly_utilization": sum(self.monthly_spend.values()) / max(self.monthly_budget, 0.01),
        }
        return status

    def _alert(self, alert_type: str, severity: str, persona: str,
               details: str, current: float, limit: float):
        self.alerts.append(CostAlert(
            timestamp=datetime.now(timezone.utc).isoformat(),
            alert_type=alert_type,
            severity=severity,
            persona=persona,
            details=details,
            current_spend=current,
            budget_limit=limit,
        ))


