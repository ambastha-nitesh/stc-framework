class CostAnomalyDetector:
    """
    Detects cost anomalies by comparing individual requests
    against a rolling average.
    """

    def __init__(self, threshold_multiplier: float = 5.0, window_size: int = 100):
        self.threshold = threshold_multiplier
        self.window_size = window_size
        self.history: dict[str, deque] = defaultdict(lambda: deque(maxlen=window_size))

    def check(self, persona: str, model: str, cost_usd: float) -> tuple[bool, str]:
        """Check if a cost is anomalous."""
        key = f"{persona}:{model}"
        history = self.history[key]

        if len(history) < 10:
            history.append(cost_usd)
            return False, "Insufficient history"

        avg = sum(history) / len(history)
        if avg > 0 and cost_usd > avg * self.threshold:
            return True, f"Cost anomaly: ${cost_usd:.4f} is {cost_usd/avg:.1f}× the rolling avg ${avg:.4f}"

        history.append(cost_usd)
        return False, "Normal"

    def record(self, persona: str, model: str, cost_usd: float):
        key = f"{persona}:{model}"
        self.history[key].append(cost_usd)


