class RateLimiter:
    """
    Token bucket rate limiter per persona.
    Prevents any single persona from overwhelming the system.
    """

    def __init__(self, spec: STCSpec):
        self.spec = spec
        rate_config = spec.trainer.get("rate_limits", {})

        # Defaults if not in spec
        self.limits = {
            "stalwart": {
                "rpm": rate_config.get("stalwart_rpm", 30),    # requests per minute
                "rph": rate_config.get("stalwart_rph", 500),   # requests per hour
                "concurrent": rate_config.get("stalwart_concurrent", 5),
            },
            "trainer": {
                "rpm": rate_config.get("trainer_rpm", 10),
                "rph": rate_config.get("trainer_rph", 100),
                "concurrent": rate_config.get("trainer_concurrent", 2),
            },
            "critic": {
                "rpm": rate_config.get("critic_rpm", 60),      # Critic needs to be fast
                "rph": rate_config.get("critic_rph", 1000),
                "concurrent": rate_config.get("critic_concurrent", 10),
            },
        }

        # Tracking windows
        self.minute_windows: dict[str, deque] = defaultdict(lambda: deque())
        self.hour_windows: dict[str, deque] = defaultdict(lambda: deque())
        self.active_requests: dict[str, int] = defaultdict(int)

    def check(self, persona: str) -> tuple[bool, str, float]:
        """
        Check if a request is allowed under rate limits.
        Returns (allowed, reason, wait_seconds).
        """
        limits = self.limits.get(persona, self.limits.get("stalwart"))
        now = time.time()

        # Clean old entries
        minute_ago = now - 60
        hour_ago = now - 3600

        window = self.minute_windows[persona]
        while window and window[0] < minute_ago:
            window.popleft()

        hour_window = self.hour_windows[persona]
        while hour_window and hour_window[0] < hour_ago:
            hour_window.popleft()

        # Check concurrent
        if self.active_requests[persona] >= limits["concurrent"]:
            return False, f"Concurrent limit reached ({limits['concurrent']})", 1.0

        # Check RPM
        if len(window) >= limits["rpm"]:
            wait = 60 - (now - window[0])
            return False, f"Rate limit: {limits['rpm']} RPM exceeded", max(wait, 0.1)

        # Check RPH
        if len(hour_window) >= limits["rph"]:
            wait = 3600 - (now - hour_window[0])
            return False, f"Rate limit: {limits['rph']} RPH exceeded", max(wait, 0.1)

        return True, "Within rate limits", 0.0

    def record_request_start(self, persona: str):
        now = time.time()
        self.minute_windows[persona].append(now)
        self.hour_windows[persona].append(now)
        self.active_requests[persona] += 1

    def record_request_end(self, persona: str):
        self.active_requests[persona] = max(0, self.active_requests[persona] - 1)


