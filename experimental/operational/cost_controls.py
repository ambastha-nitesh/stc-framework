class CostEstimator:
    """Estimates cost before making an LLM call."""

    # Cost per 1M tokens (input/output) — updated as models change
    MODEL_COSTS = {
        # Anthropic
        "anthropic/claude-sonnet": {"input": 3.00, "output": 15.00},
        "anthropic/claude-haiku": {"input": 0.25, "output": 1.25},
        "bedrock/claude-sonnet": {"input": 3.00, "output": 15.00},
        # OpenAI
        "openai/gpt-4o": {"input": 2.50, "output": 10.00},
        "openai/gpt-4o-mini": {"input": 0.15, "output": 0.60},
        # Local (free)
        "local/llama-3.3-70b-instruct": {"input": 0.00, "output": 0.00},
        "local/mistral-large-instruct": {"input": 0.00, "output": 0.00},
        "ollama/llama3.3:70b-instruct": {"input": 0.00, "output": 0.00},
    }

    def estimate(self, model: str, input_tokens: int, output_tokens: int) -> float:
        """Estimate cost in USD for a given request."""
        costs = self.MODEL_COSTS.get(model)
        if not costs:
            # Try partial match
            for key, val in self.MODEL_COSTS.items():
                if key.split("/")[-1] in model or model in key:
                    costs = val
                    break
        if not costs:
            costs = {"input": 5.00, "output": 15.00}  # Conservative default

        input_cost = (input_tokens / 1_000_000) * costs["input"]
        output_cost = (output_tokens / 1_000_000) * costs["output"]
        return input_cost + output_cost


