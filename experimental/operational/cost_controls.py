class OperationalController:
    """
    Unified operational control interface for the STC Framework.
    
    Coordinates token governance, rate limiting, burst control,
    cost circuit breakers, and anomaly detection.
    """

    def __init__(self, spec: STCSpec):
        self.spec = spec
        self.cost_estimator = CostEstimator()
        self.token_governor = TokenGovernor(spec)
        self.rate_limiter = RateLimiter(spec)
        self.burst_controller = BurstController(spec)
        self.circuit_breaker = CostCircuitBreaker(spec)
        self.anomaly_detector = CostAnomalyDetector()
        self.usage_history: list[UsageRecord] = []

    def pre_request_check(self, persona: str, model: str,
                           estimated_input_tokens: int,
                           estimated_output_tokens: int,
                           workflow_id: Optional[str] = None) -> PreRequestApproval:
        """
        Run all pre-request checks before making an LLM call.
        This is the single entry point for operational control.
        """
        # 1. Token governance
        token_ok, token_reason = self.token_governor.check(
            estimated_input_tokens, estimated_output_tokens
        )
        if not token_ok:
            return PreRequestApproval(
                approved=False, action="reject", reason=token_reason,
                estimated_cost_usd=0.0, budget_remaining_usd=0.0,
                constraints={"check": "token_governance"},
            )

        # 2. Rate limiting
        rate_ok, rate_reason, wait = self.rate_limiter.check(persona)
        if not rate_ok:
            return PreRequestApproval(
                approved=False, action="throttle", reason=rate_reason,
                estimated_cost_usd=0.0, budget_remaining_usd=0.0,
                wait_seconds=wait,
                constraints={"check": "rate_limit"},
            )

        # 3. Burst control
        if workflow_id:
            burst_ok, burst_reason = self.burst_controller.check_workflow(workflow_id)
            if not burst_ok:
                return PreRequestApproval(
                    approved=False, action="reject", reason=burst_reason,
                    estimated_cost_usd=0.0, budget_remaining_usd=0.0,
                    constraints={"check": "burst_control"},
                )

        # 4. Cost estimation
        estimated_cost = self.cost_estimator.estimate(
            model, estimated_input_tokens, estimated_output_tokens
        )

        # 5. Cost circuit breaker
        cost_action, cost_reason, cost_wait = self.circuit_breaker.check(persona, estimated_cost)

        if cost_action == "halt":
            return PreRequestApproval(
                approved=False, action="reject", reason=cost_reason,
                estimated_cost_usd=estimated_cost, budget_remaining_usd=0.0,
                constraints={"check": "cost_circuit_breaker"},
            )

        if cost_action == "pause":
            return PreRequestApproval(
                approved=False, action="queue", reason=cost_reason,
                estimated_cost_usd=estimated_cost, budget_remaining_usd=0.0,
                wait_seconds=cost_wait,
                constraints={"check": "cost_circuit_breaker"},
            )

        # Calculate remaining budget
        budget_status = self.circuit_breaker.get_budget_status()
        persona_status = budget_status.get(persona, {})
        remaining = persona_status.get("daily_budget", 0) - persona_status.get("daily_spent", 0)

        # Approved (possibly with warning or throttle)
        approved_action = "allow"
        if cost_action in ("warn", "throttle"):
            approved_action = cost_action

        # Record rate limit tracking
        self.rate_limiter.record_request_start(persona)
        if workflow_id:
            self.burst_controller.record_llm_call(workflow_id)

        return PreRequestApproval(
            approved=True,
            action=approved_action,
            reason=cost_reason,
            estimated_cost_usd=estimated_cost,
            budget_remaining_usd=remaining,
            wait_seconds=cost_wait,
            constraints={"model": model, "estimated_tokens": estimated_input_tokens + estimated_output_tokens},
        )

    def record_usage(self, persona: str, model: str,
                     input_tokens: int, output_tokens: int,
                     cost_usd: float, latency_ms: float):
        """Record actual usage after a request completes."""
        record = UsageRecord(
            timestamp=datetime.now(timezone.utc).isoformat(),
            persona=persona,
            model=model,
            input_tokens=input_tokens,
            output_tokens=output_tokens,
            total_tokens=input_tokens + output_tokens,
            cost_usd=cost_usd,
            latency_ms=latency_ms,
        )
        self.usage_history.append(record)

        # Update circuit breaker
        self.circuit_breaker.record_spend(persona, cost_usd)

        # Update rate limiter
        self.rate_limiter.record_request_end(persona)

        # Check for anomaly
        is_anomaly, anomaly_reason = self.anomaly_detector.check(persona, model, cost_usd)
        if is_anomaly:
            logger.warning(f"💰 COST ANOMALY: {anomaly_reason}")

        self.anomaly_detector.record(persona, model, cost_usd)

    def get_operational_status(self) -> dict:
        """Get comprehensive operational status."""
        budget = self.circuit_breaker.get_budget_status()

        # Recent usage stats
        recent = self.usage_history[-100:]
        total_cost = sum(r.cost_usd for r in recent)
        total_tokens = sum(r.total_tokens for r in recent)

        return {
            "budget": budget,
            "rate_limits": {
                persona: {
                    "active_requests": self.rate_limiter.active_requests.get(persona, 0),
                    "minute_count": len(self.rate_limiter.minute_windows.get(persona, deque())),
                    "limit_rpm": self.rate_limiter.limits.get(persona, {}).get("rpm", 0),
                }
                for persona in ["stalwart", "trainer", "critic"]
            },
            "recent_usage": {
                "requests": len(recent),
                "total_cost_usd": total_cost,
                "total_tokens": total_tokens,
                "avg_cost_per_request": total_cost / max(len(recent), 1),
                "avg_tokens_per_request": total_tokens / max(len(recent), 1),
            },
            "alerts": len(self.circuit_breaker.alerts),
            "recent_alerts": [
                {"type": a.alert_type, "severity": a.severity, "persona": a.persona, "details": a.details}
                for a in self.circuit_breaker.alerts[-5:]
            ],
        }


